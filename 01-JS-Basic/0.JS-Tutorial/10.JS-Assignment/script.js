//JS Assignment

//=
let x = 10;
console.log(x);

//+=
let x1 = 10;
x1+=5;
console.log(x1);

//-=
let x2=10;
x2-=5;
console.log(x2);

//*=
let x3=10;
x3*=5;
console.log(x3);


// **=
let x4=10;
x4**=5;
console.log(x4);

// /=
let x5=10;
x5/=5;
console.log(x5);

// %=
let x6=10;
x6%=5;
console.log(x6);


//Shift Assignment Operators

// <<=
let y1 = -100;
y1 <<= 5;
console.log(y1);


// >>=
let y2 = -100;
y2 >>= 5;
console.log(y2);


// >>>=
let y3 = -100;
y3 >>>= 5;
console.log(y3);



//Bitwise Assignment Operators -ES6

