console.log(3 % 2); // Modulus - finding remainder
console.log(3 ** 2); // Exponentiation 3 ** 2 == 3 * 3

//1.Addition
let a = 100 + 50;
console.log(a); //150

//2.Subtraction
let b = 100 - 50;
console.log(b); //50

//3.Multiplication
let c = 100 * 50;
console.log(c); //5000

//4.Division
let d = 100 / 50;
console.log(d); //2

//5.Remainder
let e = 100 % 50;
console.log(e); //0

//6.incrementing
let f = 5;
f++;
console.log(f); //6

//7.Decrementing
let g = 5;
g--;
console.log(g); //4

//8.Exponentiation
let h = 100;
let i = h ** 2;
console.log(i); //1000
