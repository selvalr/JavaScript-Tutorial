//Conditional (Ternary) Operator

/*
Syntax:

variablename = (condition) ? value1:value2 
*/



let age = 12;
let voteable = (age < 18) ? "Too young":"Old enough";
console.log(voteable + " to vote.");


//1.example
let a = 10, b = 5;
let c = a > b? a : b; 
console.log(c);
let d = a > b? b : a; 
console.log(d);
